#include <iostream>
using namespace std;

int main() {
    int a = 100, x = -50;
    float b = 20.55, y = -3.14;
    double c = 99.999999;
    char d = 'Z';
    bool e = true, f = false;
    string g = "Hello, World!";

    cout << "Integer Values: " << a << ", " << x << endl;
    cout << "Float Values: " << b << ", " << y << endl;
    cout << "Double Value: " << c << endl;
    cout << "Character: " << d << endl;
    cout << "Boolean Values: " << e << ", " << f << endl;
    cout << "String: " << g << endl;


    return 0;
}
